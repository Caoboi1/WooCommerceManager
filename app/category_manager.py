    def on_item_changed(self, item):
        """X·ª≠ l√Ω khi m·ªôt item trong table ƒë∆∞·ª£c ch·ªânh s·ª≠a"""
        try:
            # NgƒÉn dialog hi·ªÉn th·ªã khi ƒëang kh·ªüi t·∫°o
            if hasattr(self, 'is_initializing') and self.is_initializing:
                return

            row = item.row()
            column = item.column()

            # L·∫•y category data t·ª´ row ƒë·∫ßu ti√™n
            id_item = self.table.item(row, 0)
            if not id_item:
                return

            category_data = id_item.data(Qt.ItemDataRole.UserRole)
            if not category_data:
                return

            category_id = category_data.get('id')
            wc_category_id = category_data.get('wc_category_id')
            new_value = item.text().strip()

            # X√°c ƒë·ªãnh field n√†o ƒë∆∞·ª£c ch·ªânh s·ª≠a
            field_mapping = {
                2: 'name',  # T√™n danh m·ª•c
                3: 'slug',  # Slug
                4: 'description'  # M√¥ t·∫£
            }

            # T√™n hi·ªÉn th·ªã cho c√°c tr∆∞·ªùng
            field_display_names = {
                'name': 't√™n danh m·ª•c',
                'slug': 'slug',
                'description': 'm√¥ t·∫£'
            }

            field_name = field_mapping.get(column)
            if not field_name:
                return

            # Ki·ªÉm tra xem c√≥ thay ƒë·ªïi th·ª±c s·ª± kh√¥ng
            old_value = category_data.get(field_name, '')
            if new_value == old_value:
                return

            # C·∫≠p nh·∫≠t category data
            updated_data = category_data.copy()
            updated_data[field_name] = new_value

            # N·∫øu category ƒë√£ ƒë·ªìng b·ªô v·ªõi WooCommerce, c·∫≠p nh·∫≠t tr·ª±c ti·∫øp
            if wc_category_id:
                field_display = field_display_names.get(field_name, field_name)
                reply = QMessageBox.question(
                    self, "C·∫≠p nh·∫≠t tr·ª±c ti·∫øp",
                    f"B·∫°n c√≥ mu·ªën c·∫≠p nh·∫≠t {field_display} l√™n WooCommerce ngay?\n\n"
                    f"Gi√° tr·ªã c≈©: {old_value}\n"
                    f"Gi√° tr·ªã m·ªõi: {new_value}", QMessageBox.StandardButton.Yes
                    | QMessageBox.StandardButton.No,
                    QMessageBox.StandardButton.Yes)

                if reply == QMessageBox.StandardButton.Yes:
                    # C·∫≠p nh·∫≠t tr·ª±c ti·∫øp l√™n site
                    self.update_category_inline(category_id, updated_data,
                                                wc_category_id, field_name)
                else:
                    # Ch·ªâ c·∫≠p nh·∫≠t local
                    self.db.update_category(category_id, updated_data)
                    # C·∫≠p nh·∫≠t category_data trong item
                    id_item.setData(Qt.ItemDataRole.UserRole, updated_data)
            else:
                # Ch·ªâ c·∫≠p nh·∫≠t local cho category ch∆∞a ƒë·ªìng b·ªô
                self.db.update_category(category_id, updated_data)
                # C·∫≠p nh·∫≠t category_data trong item
                id_item.setData(Qt.ItemDataRole.UserRole, updated_data)

                # Hi·ªÉn th·ªã th√¥ng b√°o
                self.status_label.setText(
                    f"ƒê√£ c·∫≠p nh·∫≠t {field_name} trong database local")

        except Exception as e:
            self.logger.error(f"L·ªói c·∫≠p nh·∫≠t inline: {str(e)}")
            QMessageBox.critical(self, "L·ªói", f"Kh√¥ng th·ªÉ c·∫≠p nh·∫≠t: {str(e)}")
            # Kh√¥i ph·ª•c gi√° tr·ªã c≈©
            if 'old_value' in locals():
                item.setText(old_value)

    def update_category_inline(self, category_id: int, category_data: Dict,
                               wc_category_id: int, field_name: str):
        """C·∫≠p nh·∫≠t category inline l√™n WooCommerce"""
        try:
            # Disable editing t·∫°m th·ªùi
            self.table.setEnabled(False)
            self.status_label.setText(
                f"ƒêang c·∫≠p nh·∫≠t {field_name} l√™n WooCommerce...")

            # L·∫•y th√¥ng tin site
            site_id = category_data.get('site_id')
            site = self.db.get_site_by_id(site_id)
            if not site:
                raise Exception("Kh√¥ng t√¨m th·∫•y th√¥ng tin site")

            # Kh·ªüi t·∫°o API
            from .woocommerce_api import WooCommerceAPI
            api = WooCommerceAPI(site)

            # Chu·∫©n b·ªã d·ªØ li·ªáu c·∫≠p nh·∫≠t
            wc_update_data = {field_name: category_data[field_name]}

            # N·∫øu c·∫≠p nh·∫≠t slug, c·∫ßn validate
            if field_name == 'slug':
                import re
                slug = re.sub(r'[^a-zA-Z0-9\s-]', '',
                              category_data[field_name].lower())
                slug = re.sub(r'\s+', '-', slug.strip())
                wc_update_data['slug'] = slug
                category_data['slug'] = slug

            # C·∫≠p nh·∫≠t l√™n WooCommerce
            result = api.update_category(wc_category_id, wc_update_data)

            if result:
                # C·∫≠p nh·∫≠t database local
                self.db.update_category(category_id, category_data)

                # C·∫≠p nh·∫≠t item data
                id_item = self.table.item(self.table.currentRow(), 0)
                if id_item:
                    id_item.setData(Qt.ItemDataRole.UserRole, category_data)

                self.status_label.setText(
                    f"‚úÖ ƒê√£ c·∫≠p nh·∫≠t {field_name} th√†nh c√¥ng!")

                # Auto refresh sau 2 gi√¢y
                QTimer.singleShot(
                    2000, lambda: self.status_label.setText("S·∫µn s√†ng"))
            else:
                raise Exception("Kh√¥ng nh·∫≠n ƒë∆∞·ª£c response t·ª´ WooCommerce")

        except Exception as e:
            self.logger.error(f"L·ªói c·∫≠p nh·∫≠t inline l√™n site: {str(e)}")

            # H·ªèi c√≥ mu·ªën c·∫≠p nh·∫≠t local kh√¥ng
            reply = QMessageBox.question(
                self, "C·∫≠p nh·∫≠t th·∫•t b·∫°i",
                f"Kh√¥ng th·ªÉ c·∫≠p nh·∫≠t l√™n WooCommerce:\n{str(e)}\n\n"
                "B·∫°n c√≥ mu·ªën l∆∞u thay ƒë·ªïi trong database local kh√¥ng?",
                QMessageBox.StandardButton.Yes | QMessageBox.StandardButton.No,
                QMessageBox.StandardButton.Yes)

            if reply == QMessageBox.StandardButton.Yes:
                self.db.update_category(category_id, category_data)
                self.status_label.setText(
                    "ƒê√£ l∆∞u thay ƒë·ªïi trong database local")
            else:
                # Reload ƒë·ªÉ kh√¥i ph·ª•c gi√° tr·ªã c≈©
                self.load_categories()

        finally:
            # Re-enable editing
            self.table.setEnabled(True)

